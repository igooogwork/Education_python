# Пользователь делает вклад в размере a рублей сроком
# на years лет под 10% годовых (каждый год размер его вклада
# увеличивается на 10%. Эти деньги прибавляются к сумме вклада,
# и на них в следующем году тоже будут проценты). 
# Написать функцию bank, принимающая аргументы a и years,
# и возвращающую сумму, которая будет на счету пользователя.
a = input('Введите количество рублей:')
a = int(a)
years = input('На сколько лет:')
years = int(years)
i = 0
bank = a
while i < years:
    i += 1
    bank = bank * 1.1
print(round(bank, 2))